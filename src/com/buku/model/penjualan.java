public class penjualan {
    private String kodeStruk;
    private String waktu;
    private String tgl;
    private String userKasir;
    private String uangBayar;
    private String uangKembalian;
    private String totalBayar;

    public String getKodeStruk() {
        return kodeStruk;
    }

    public void setKodeStruk(String kodeStruk) {
        this.kodeStruk = kodeStruk;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getTgl() {
        return tgl;
    }

    public void setTgl(String tgl) {
        this.tgl = tgl;
    }

    public String getUserKasir() {
        return userKasir;
    }

    public void setUserKasir(String userKasir) {
        this.userKasir = userKasir;
    }

    public String getUangBayar() {
        return uangBayar;
    }

    public void setUangBayar(String uangBayar) {
        this.uangBayar = uangBayar;
    }

    public String getUangKembalian() {
        return uangKembalian;
    }

    public void setUangKembalian(String uangKembalian) {
        this.uangKembalian = uangKembalian;
    }

    public String getTotalBayar() {
        return totalBayar;
    }

    public void setTotalBayar(String totalBayar) {
        this.totalBayar = totalBayar;
    }
    
    
    
}
